produto_1 = 20
produto_2 = 10

print(produto_1 + produto_2 + 3.5) #adição

print(produto_1 - produto_2 - 3.5) #subtração

print(produto_1 * produto_2 * 3.5) #multiplicação

print(produto_1 / produto_2 / 3.5) #divisão

print(produto_1 // produto_2) #divisão com o resto interio

print(produto_1 % produto_2) # mudulo, resto da divisão

print(produto_1 ** produto_2) #exponenciação

