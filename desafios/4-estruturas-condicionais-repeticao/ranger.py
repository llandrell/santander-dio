# Laço de repetição com for e range
# range(início, fim, passo): gera uma sequência de números começando de 'início', até 'fim' (exclusive), pulando de 'passo' em 'passo'

for numero in range(0, 21, 2):  # Começa em 0, vai até 20 (21 não entra), pulando de 2 em 2
    print(numero, end=" ")      # Imprime os números na mesma linha, separados por espaço
